public class StringExercise {
    public static void main(String[] args) {
        String education = "We don't need no education\nWe don't need no thought control";
        System.out.println(education);

        String checkIt = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkIt);

        String drive = "In Windows, the main drive is usually C:\\";
        System.out.println(drive);

        String backslashes = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);
    }
}
